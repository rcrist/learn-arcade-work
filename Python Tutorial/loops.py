# For loop
for i in range(5):  # Count from 0 to 4
    print("I will not chew gum in class.")

for j in range(2, 6):  # Count from 2 to 5
    print(j)

for k in range(5, 26, 5):  # Count from 5 to 25 with a step of 5
    print(k)

# Keep a running total
total = 0
for n in range(5):
    total = total + n
    print(total)

# While loop
n = 1
while n < 6:
    print(n)
    n += 1
